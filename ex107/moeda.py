def metade(preco):
  metade = preco / 2
  return metade

def dobro(preco):
  dobro = preco * 2
  return dobro

def aumentar(preco, porcentagem):
  aumentar = preco + (preco * (porcentagem/100))
  return aumentar

def diminuir(preco,porcetagem):
  diminuir = preco - (preco * (porcetagem/100))
  return diminuir